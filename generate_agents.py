    if category not in categories:
        categories[category] = []
    
    existing_names = {agent["name"] for agent in categories[category]}
    
    for i, (role, personality) in enumerate(new_agents_data):
        # ユニークな名前を生成
        base_name = f"{category}エージェント{i+1}"
        name = base_name
        counter = 1
        while name in existing_names:
            name = f"{base_name}_{counter}"
            counter += 1
        
        categories[category].append({
            "name": name,
            "role": role,
            "personality": personality
        })
        existing_names.add(name)

